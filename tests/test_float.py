# Test float type
x = 3.14
print(x)
print(1.0)
print(0.5)
print(-2.5)

# Arithmetic
print(x + 1.0)
print(x - 1.0)
print(x * 2.0)
print(x / 2.0)
print(7.0 // 2.0)
print(7.0 % 2.0)
print(-x)

# Int-float mixed arithmetic
print(x + 1)
print(1 + x)
print(x * 2)
print(10 - x)
print(x / 2)

# Comparisons
print(1.0 == 1.0)
print(1.0 != 2.0)
print(1.0 < 2.0)
print(2.0 > 1.0)
print(1.0 <= 1.0)
print(2.0 >= 1.0)

# Int-float comparison
print(1.0 == 1)
print(1 == 1.0)
print(1 < 1.5)
print(2.5 > 2)

# Bool conversion
print(bool(1.0))
print(bool(0.0))

# float() builtin
print(float(42))
print(float())
print(float(3.14))

# type()
print(type(3.14))
